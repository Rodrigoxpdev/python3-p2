# CONVERSOR DE MOEDAS DE REAL 'R$' PARA DOLAR 'U$'! 
real = float(input('Quanto dinheiro voce tem na carteira ? R$'))

#CALCULO VALOR DIGITADO DIVIDIDO PELO VALOR DO DOLAR ATUALMENTE!
dolar = real / 5.43

#RESULTADO
print('Com R${:.2f} voce pode comprar U${:.2f} '.format(real, dolar))




























