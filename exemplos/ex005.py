# ---DISSECANDO UMA VARIAVEL---
a = input('digite algo: ')
print('É STRING? ', a.isalpha())
print('É UM NUMERO? ', a.isnumeric())
print('CONTEN SÓ ESPAÇOS? ', a.isspace())
print('É ALPHA NUMERICO? ', a.isalnum())
print('ESTA EM MAIUSCULAS? ', a.isupper())
print('ESTA EM MAIUSCULAS? ', a.islower())
print('ESTA EM CAPITALIZADA? ', a.istitle())

# isalpha() = STRING TEXTO
# isspace() = CONTEM SÓ ESPAÇOS?
# isnumeric() = NUMEROS
# isalnum() = É ALPHA NUMERICO?
# isupper() = ESTA EM MAIUSCULAS?
# islower() = ESTA EM MAIUSCULAS?
# istitle() = ESTA EM CAPITALIZADA, MISTA = 'A a'?

