#FAZENDO PERGUNTAS!
larg = float(input('Largura da parede: '))
alt = float(input('Altura da parede: '))

#CALCULO
area = larg * alt
tinta = area / 2

#RESULTADO:
print('Sua parede tem a dimensao de {}x{} e sua area é de {}m2. \nPara pintar essa parede, você precisará de {}L de tinta.'.format(larg, alt, area, tinta))


































