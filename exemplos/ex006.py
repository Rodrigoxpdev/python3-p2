# ---OPERADORES PY---
# + = ADIÇAO
# - = SUBTRAÇAO
# * = MULTIPLICAÇAO
# / = DIVISAO
# ** = POTENCIA
# // = DIVISAO INTEIRA
# % = RESTO DA DIVISAO

# MEDIA ARITIMETICA
n1 = float(input('Primeira media? '))
n2 = int(input('Segunda media? '))

#ONDEM DE PRECEDENCIA E '()' DPS' /'
med = (n1 + n2) / 2

# RESULTADO
print('A media entre {:.1f} e {:.1f} é igual a {:.1f}'.format(n1, n2 , med))













































